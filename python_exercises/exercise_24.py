# Weekday Name


def weekday_name(weekday):
    if(weekday == 1):
        result = "Sunday"
    elif(weekday == 2):
        result = "Monday"
    elif(weekday == 3):
        result = "Tuesday"
    elif(weekday == 4):
        result = "Wednesday"
    elif(weekday == 5):
        result = "Thursday"
    elif(weekday == 6):
        result = "Friday"
    elif(weekday == 7):
        result = "Saturday"

    return result


