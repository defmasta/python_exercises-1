import time

def read_in_map(data):

    # split string at every new line
    map_split = data.split('\n')

    # delete empty elements in the list
    clean_map = [x for x in map_split if x]

    # print sample slopes
    n = 1
    print("First 25 slopes..\n")
    for i in clean_map:
        print(i)
        n += 1
        if n == 25+1:
            break

    # find how many slope repetitions are needed
    print("\nOne slope has {} characters.".format(len(clean_map[0])))
    slopes_num = len(clean_map)
    print("Number of slopes:", slopes_num)
    min_char = slopes_num * 3
    print("Slope must be at least {} characters long.".format(min_char))
    rep = (min_char // len(clean_map[0])) + 1
    print("That is {} repetitions.".format(rep))

    # create map with repeated slopes
    full_map = []
    for i in clean_map:
        rep_slice = i * rep
        full_map.append(rep_slice)

    time.sleep(3)
    return full_map


def check_slope(data):

    map = data

    n = 0
    left = 3
    tree = 0

    while n < len(map):
        if n > 0:
            if map[n][left] == '#':
                print(map[n][:left]+"X"+map[n][left:])
                tree += 1
            else:
                print(map[n][:left] + "O" + map[n][left:])
            left += 3
        n += 1

    print("There are {} trees.".format(tree))


